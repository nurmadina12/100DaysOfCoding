
package dayss056;
public class Dayss056 {
    public static void main(String[] args) {
        int n = 1;
        System.out.println("===Mencetak angka dari 1 sampai 6 menggunakan do-while===");
        do{
            System.out.println("Angka "+n);
            n++;
        }while (n<=6);
        System.out.println("Nilai akhir n adalah "+n);
    }
    
}
